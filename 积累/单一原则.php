/*面向对象的软件开发中，有一个基本原则，那就是单一原则，是设计模式的重点。单一原则，功能单一的类，避免万能类。如果一个类的功能多于一点，就应该拆分成2个类。
 * 是面向对象的设计模式中最重要的一个原则。举个例子，在智能手机刚刚出现的时候，诺基亚占据世界大半壁江山。智能手机可以打电话，发短信，浏览网页，玩游戏，拍照，录像等等，
 * 但是，拿拍照来说，拍照比不过傻瓜相机（如今也比不过单反）。尽管将大量的功能融合为一台设备，携带和充电更方便，但是效果并不如单一功能的强大。这就引入了“单一原则”。
 * 在软件开发过程中，单一原则是设计模式中非常重要的思想。如果，你能够在一个类中找到多于一个的功能，那么，这个类就该进行抽象和拆分了。在OOP中有一个大忌讳，就是万能类。
 * 一个成千上万行的类，臃肿而庞大，为什么不柴分成多个类呢？每个类负责一个功能，各思其职。*/